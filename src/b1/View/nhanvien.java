    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        int row = tblBangNV.getSelectedRow();
        b1.entity.nhanvien b1env = list.get(row);
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon xoa nhan vien khong");
        if (confirm == JOptionPane.YES_OPTION) {
            service.deleteNhanVien(b1env.getId());
            list = service.getAll();
            showDataTable(list);
            JOptionPane.showMessageDialog(this, "Xoa nhan vien thanh cong");
        }
        if (confirm == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn không");
            return;
        }
        if (confirm == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn Cancel");
            return;
        }
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon them nhan vien khong");
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                JOptionPane.showMessageDialog(this, service.addNhanVien(getFormData()));
                list = service.getAll();
                showDataTable(list);
            } catch (ParseException ex) {
                Logger.getLogger(nhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        if (confirm == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn không");
            return;
        }
        if (confirm == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn Cancel");
            return;
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon sua nhan vien khong");
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                int row = tblBangNV.getSelectedRow();
                b1.entity.nhanvien b1env = list.get(row);
                service.updateNhanVien(getFormData(), b1env.getId());
                list = service.getAll();
                showDataTable(list);
            } catch (ParseException ex) {
                Logger.getLogger(nhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        if (confirm == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn không");
            return;
        }
        if (confirm == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Bạn đã chọn Cancel");
            return;
        }
    }//GEN-LAST:event_btnSuaActionPerformed

